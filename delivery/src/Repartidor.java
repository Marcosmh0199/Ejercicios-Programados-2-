public class Repartidor {
  private long tiempoKilometro;
  
  public Repartidor(long tiempoKilometro){
    setTiempoKilometro(tiempoKilometro);
  }

  public long getTiempoKilometro() {
    return tiempoKilometro;
  }
  public void setTiempoKilometro(long tiempoKilometro) {
    this.tiempoKilometro = tiempoKilometro;
  }
}
